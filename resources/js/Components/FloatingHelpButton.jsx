import { useState } from 'react';
import useLocale from '../hooks/useLocale';
import ChatbotWidget from './ChatbotWidget';

export default function FloatingHelpButton() {
  const { isRtl } = useLocale();
  const [chatOpen, setChatOpen] = useState(false);

  return (
    <>
      <ChatbotWidget open={chatOpen} onClose={() => setChatOpen(false)} />

      <button
        onClick={() => setChatOpen(!chatOpen)}
        className={`fixed z-40 ${isRtl ? 'start-4 sm:start-6' : 'end-4 sm:end-6'} bottom-4 sm:bottom-6 flex h-14 w-14 items-center justify-center rounded-full bg-gold text-night shadow-lg shadow-gold/30 transition-all hover:scale-105 hover:shadow-xl hover:shadow-gold/40 active:scale-95`}
        aria-label="Help"
      >
        {chatOpen ? (
          <svg className="h-6 w-6" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
          </svg>
        ) : (
          <svg className="h-6 w-6" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M8.625 12a.375.375 0 11-.75 0 .375.375 0 01.75 0zm0 0H8.25m4.125 0a.375.375 0 11-.75 0 .375.375 0 01.75 0zm0 0H12m4.125 0a.375.375 0 11-.75 0 .375.375 0 01.75 0zm0 0h-.375M21 12c0 4.556-4.03 8.25-9 8.25a9.764 9.764 0 01-2.555-.337A5.972 5.972 0 015.41 20.97a5.969 5.969 0 01-.474-.065 4.48 4.48 0 00.978-2.025c.09-.457-.133-.901-.467-1.226C3.93 16.178 3 14.189 3 12c0-4.556 4.03-8.25 9-8.25s9 3.694 9 8.25z" />
          </svg>
        )}
      </button>
    </>
  );
}
